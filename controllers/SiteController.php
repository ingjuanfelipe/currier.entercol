<?php

namespace app\controllers;

use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;
use app\models\Ordenes;
use app\models\Shopify;
class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        return $this->render('index');
    }
    public function actionOrdenes()
    {
       $ordenes=new Ordenes();
       $Shopify=new Shopify();
       $shopify=$_GET;
       $token="";
       if(!$ordenes->view($shopify)){
           header("Location:install.php?shop=" .$shopify['shop']);
           exit();
       }else{
           foreach ($ordenes->view($shopify['shop']) as $rows) {
            $token=$rows['access_token'];  
           }
           $shop_url=$shopify['shop'];
            //$url=$shopify;
            //$token=$shop_row['access_token'];
           // $shop_url.'<br>';
            //echo $token;
            //$ordenes=shopify_call($token,$shop_url,"/admin/api/2021-01/orders.json?status=any",array(),'GET');
            $ord=$Shopify->shopify_call($token, $shop_url, "/admin/api/2021-01/orders.json?status=any",array(),'GET');
            $ord= json_decode($ord['response'],JSON_PRETTY_PRINT);
            return $this->render('ordenes',['model'=>$ord,'url'=>$token]);
       }
        //
        
    }


    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        return $this->render('about');
    }
}
